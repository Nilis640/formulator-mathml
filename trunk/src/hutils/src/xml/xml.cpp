void _TreateMessages( const QString& _taskname, TXML_Parser*& parser, DRMessageManager& dmm, long message_page )
{
	const std::vector<HKLineMessage>& eTbl = parser->GetErrorTable();
	const std::vector<HKLineMessage>& wTbl = parser->GetWarningTable();
	std::size_t i;
	for( i = 0; i < eTbl.size(); i++ )
	{
		if( eTbl[ i ].line )
			dmm.AddLine( message_page, eTbl[ i ].text, _taskname, eTbl[ i ].line );
		else
			dmm.AddLine( message_page, eTbl[ i ].text );
	}
	for( i = 0; i < wTbl.size(); i++ )
	{
		if( wTbl[ i ].line )
			dmm.AddLine( message_page, wTbl[ i ].text, _taskname, wTbl[ i ].line );
		else
			dmm.AddLine( message_page, wTbl[ i ].text );
	}
}

int _TryCompileXML( const QString& text, AST_TXML_Compound*& bms, TXML_Parser*& parser, DRMessageManager& /*dmm*/, long /*message_page*/ )
{
	int ret = 0;
	Source src( text );
	parser = new TXML_Parser( src, ::createActionT( (LRTDATA*)tiny_xml_lrt_table ), &bms );
	try
	{
		parser->Make();
		if( parser->IsError() ) ret = -1;
	}
	catch( QString errMsg )
	{
		parser->SetError( errMsg );
		ret = -1;
	}
	if( ret != 0 && bms )
	{
		delete bms;
		bms = 0;
	}
	return ret;
}

int LS_CompileXML( const QString& _taskname, const QString& _txt, AST_TXML_Compound*& bms, DRMessageManager& dmm, long message_page )
{
	int ret;
	TXML_Parser *parser = 0;
	ret = ::_TryCompileXML( _txt, bms, parser, dmm, message_page );
	if( parser )
	{
		::_TreateMessages( _taskname, parser, dmm, message_page );
		delete parser;
	}
	return ret;
}

